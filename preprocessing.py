class VideoPreprocessor:
    def __init__(self, config):
        self.config = config
        print("Préprocesseur initialisé (sans détection faciale)")

    def extract_frames_from_video(self, video_path):
        """Extrait les frames à 4 fps"""
        cap = cv2.VideoCapture(video_path)

        if not cap.isOpened():
            return np.array([])

        original_fps = cap.get(cv2.CAP_PROP_FPS)
        if original_fps <= 0:
            original_fps = 30

        total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
        duration = total_frames / original_fps

        target_frame_count = min(
            self.config["max_frames_per_video"],
            int(duration * self.config["target_fps"])
        )

        if target_frame_count <= 0:
            target_frame_count = 30

        frames = []

        # Méthode plus simple: prendre des frames à intervalle régulier
        frame_interval = max(1, int(original_fps / self.config["target_fps"]))

        frame_count = 0
        while len(frames) < target_frame_count and frame_count < total_frames:
            cap.set(cv2.CAP_PROP_POS_FRAMES, frame_count)
            ret, frame = cap.read()

            if not ret:
                break

            # Redimensionnement direct (sans détection faciale)
            frame_resized = cv2.resize(frame, self.config["target_size"])
            frame_rgb = cv2.cvtColor(frame_resized, cv2.COLOR_BGR2RGB)
            frames.append(frame_rgb)

            frame_count += frame_interval

        cap.release()

        # Padding si nécessaire
        if len(frames) < target_frame_count and len(frames) > 0:
            last_frame = frames[-1]
            while len(frames) < target_frame_count:
                frames.append(last_frame.copy())

        if len(frames) == 0:
            return np.array([])

        return np.array(frames, dtype=np.uint8)

    def process_all_videos(self):
        """Parcourt les dossiers"""
        stats = {
            "total_videos": 0,
            "processed": 0,
            "failed": 0,
            "total_frames": 0,
            "frames_per_video": [],
            "by_emotion": {}
        }

        for emotion_folder in self.config["emotion_folders"]:
            emotion_path = Path(self.config["input_dir"]) / emotion_folder
            output_emotion_dir = Path(self.config["output_dir"]) / emotion_folder

            if not emotion_path.exists():
                print(f"Dossier non trouvé: {emotion_path}")
                continue

            os.makedirs(output_emotion_dir, exist_ok=True)

            video_files = list(emotion_path.glob("*.mp4")) + \
                         list(emotion_path.glob("*.MP4")) + \
                         list(emotion_path.glob("*.avi")) + \
                         list(emotion_path.glob("*.mov"))

            print(f"\nDossier {emotion_folder}: {len(video_files)} vidéos trouvées")

            stats["by_emotion"][emotion_folder] = {
                "found": len(video_files),
                "processed": 0,
                "failed": 0,
                "frames": 0
            }

            for video_path in tqdm(video_files, desc=f"Traitement {emotion_folder}"):
                stats["total_videos"] += 1

                output_filename = video_path.stem + ".npy"
                output_path = output_emotion_dir / output_filename

                if output_path.exists():
                    stats["processed"] += 1
                    stats["by_emotion"][emotion_folder]["processed"] += 1
                    continue

                try:
                    frames = self.extract_frames_from_video(str(video_path))

                    if len(frames) > 0:
                        np.save(str(output_path), frames)
                        stats["processed"] += 1
                        stats["total_frames"] += len(frames)
                        stats["frames_per_video"].append(len(frames))
                        stats["by_emotion"][emotion_folder]["processed"] += 1
                        stats["by_emotion"][emotion_folder]["frames"] += len(frames)
                    else:
                        stats["failed"] += 1
                        stats["by_emotion"][emotion_folder]["failed"] += 1

                except Exception as e:
                    print(f"Erreur {video_path.name}: {e}")
                    stats["failed"] += 1
                    stats["by_emotion"][emotion_folder]["failed"] += 1

        return stats

