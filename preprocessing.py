def create_aligned_metadata(config, preprocess_stats):
    """Crée un fichier CSV alignant les vidéos traitées avec leurs métadonnées"""
    df_metadata = pd.read_csv(config["metadata_path"])
    df_phase2 = df_metadata[df_metadata["phase"] == "phase_2"].copy()

    all_videos = []

    for emotion_folder in CONFIG["emotion_folders"]:
        emotion_dir = Path(config["output_dir"]) / emotion_folder
        if not emotion_dir.exists():
            continue

        npy_files = list(emotion_dir.glob("*.npy"))

        for npy_path in npy_files:
            filename = npy_path.stem
            frames = np.load(str(npy_path))
            n_frames = frames.shape[0]

            matching_rows = df_phase2[df_phase2["filename"] == filename]

            if len(matching_rows) > 0:
                meta = matching_rows.iloc[0].to_dict()
            else:
                meta = {}

            all_videos.append({
                "filename": filename,
                "path": str(npy_path),
                "emotion_folder": emotion_folder,
                "n_frames": n_frames,
                "age": meta.get("age", None),
                "sexe": meta.get("sexe", None),
                "lunettes": meta.get("port_lunettes", None),
                "teint": meta.get("teint", None),
                "niveau_concentration": meta.get("niveau_concentration", None),
                "emotion_ciblee": meta.get("emotion", emotion_folder.lower())
            })

    df_aligned = pd.DataFrame(all_videos)
    output_csv = Path(config["output_dir"]) / "metadata_aligned.csv"
    df_aligned.to_csv(output_csv, index=False)

    print(f"\nMétadonnées alignées: {output_csv}")
    print(f"   Total vidéos: {len(df_aligned)}")
    print(df_aligned["emotion_folder"].value_counts())

    return df_aligned

