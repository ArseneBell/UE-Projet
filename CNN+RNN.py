def build_model(model_type="CNN_LSTM", num_frames=20, img_size=224):
    # On définit l'entrée proprement (Nombre de frames, Hauteur, Largeur, Couleurs)
    input_layer = layers.Input(shape=(num_frames, img_size, img_size, 3))

    base_cnn = MobileNetV2(weights='imagenet', include_top=False, input_shape=(img_size, img_size, 3))
    base_cnn.trainable = False

    # Utilisation de l'API Fonctionnelle de Keras (plus propre)
    x = layers.TimeDistributed(base_cnn)(input_layer)
    x = layers.TimeDistributed(layers.GlobalAveragePooling2D())(x)

    if model_type == "CNN_LSTM":
        x = layers.LSTM(64)(x)
    elif model_type == "CNN_GRU":
        x = layers.GRU(64)(x)
    else:
        x = layers.Flatten()(x)
        x = layers.Dense(64, activation='relu')(x)

    x = layers.Dropout(0.5)(x)
    output_layer = layers.Dense(3, activation='softmax')(x)

    model = models.Model(inputs=input_layer, outputs=output_layer, name=model_type)
    model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])
    return model

