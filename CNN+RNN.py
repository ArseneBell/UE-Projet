def final_validation(model, generator, labels):
    # 1. Prédiction
    predictions = model.predict(generator)
    y_pred = np.argmax(predictions, axis=1)

    # Ajustement des labels réels à la taille des prédictions (batch size)
    y_true = labels[:len(y_pred)]

    # 2. Rapport détaillé (Precision, Recall, F1)
